    imageAlt: 'Flat paved walking path through a park',
    title: 'Gentler gradients underfoot',
    body: 'Steep hills and uneven ground sap energy and raise the risk of a fall. Routes are chosen to keep the path as flat and smooth as possible.'
  },
  {
    image: featImg3,
    imageAlt: 'Park bench and public facilities along a path',
    title: 'Facilities shown along the way',
    body: 'Benches, drinking fountains, and public toilets along your route are marked on the map so you always know where the next stop is.'
  },
  {
    image: featImg4,
    imageAlt: 'Relaxed pedestrian street with a comfortable number of people',
    title: 'You are never walking alone',
    body: 'GreenPath favours routes through areas with a steady, comfortable flow of people. If you ever need assistance, someone is never far away.'
  },
  {
    image: featImg5,
    imageAlt: 'Older adult walking outdoors with confidence',
    title: 'Distance still matters — we factor it in',
    body: 'Within a comfortable walking range, GreenPath recommends the destination that best fits your needs today — so every step feels worth taking.'  
  }
]

let featScrollHandler = null

// ── Melbourne card gallery ────────────────────────────────────────────────────
const exploreTeaserRef      = ref(null)  // kept to avoid breaking any other code
const melSectionRef         = ref(null)
const melStageRef           = ref(null)
const melTeaserRef          = ref(null)
let melRafId          = null
let melScrollHandler  = null
let melTargetProgress = 0
let melCurrentProgress = 0
let exploreObserver   = null

const MEL_COUNT    = 12
const MEL_LEAD     = 0.10      // dwell before cards start; text fades during this phase
const MEL_DURATION = 0.40      // fraction of scroll each card animates over
const MEL_STAGGER  = 0.05      // fraction between consecutive card starts
// MEL_LEAD + (MEL_COUNT-1)*MEL_STAGGER + MEL_DURATION = 0.05 + 0.55 + 0.40 = 1.00
// → last card finishes exactly at progress=1, zero dead scroll at gallery end

// Per-card layout: finalX/Y in vw/px, finalRot in deg, launchRot (entry rotation), fold (rotateY peak)
const melCardParams = [
  { finalX:  2, finalY:  10, finalRot:  -6, launchRot:  18, fold:   8 },
  { finalX:  9, finalY:  -8, finalRot:   4, launchRot: -14, fold: -12 },
  { finalX: 17, finalY:  12, finalRot:  -9, launchRot:  22, fold:   7 },
  { finalX: 21, finalY:  -4, finalRot:   3, launchRot: -10, fold:  -9 },
  { finalX: 30, finalY:   8, finalRot:  -5, launchRot:  20, fold:  11 },
  { finalX: 34, finalY: -10, finalRot:   7, launchRot: -17, fold:  -7 },
  { finalX: 43, finalY:   6, finalRot:  -4, launchRot:  15, fold:   9 },
  { finalX: 47, finalY:  -6, finalRot:   6, launchRot: -20, fold: -13 },
  { finalX: 55, finalY:  10, finalRot:  -8, launchRot:  16, fold:   7 },
  { finalX: 58, finalY:  -3, finalRot:   5, launchRot: -11, fold: -10, dur: 0.33 },
  { finalX: 66, finalY:   7, finalRot:  -3, launchRot:  13, fold:   8, dur: 0.30 },
  { finalX: 70, finalY:  -5, finalRot:   4, launchRot: -18, fold: -11, dur: 0.27 },
]

const melCards = [
  mel01, mel02, mel03, mel04, mel05, mel06,
  mel07, mel08, mel09, mel10, mel11, mel12,
]



const easeOutBack = (t) => {
  const c1 = 1.70158, c3 = c1 + 1
  return 1 + c3 * Math.pow(t - 1, 3) + c1 * Math.pow(t - 1, 2)
}

const updateMelCards = (progress) => {
  const stage = melStageRef.value
  if (!stage) return

  const cards = stage.children
  for (let i = 0; i < cards.length; i++) {
    const el = cards[i]
    const p = melCardParams[i]
    const cStart = MEL_LEAD + i * MEL_STAGGER
    const cProg  = Math.max(0, Math.min(1, (progress - cStart) / (p.dur ?? MEL_DURATION)))

    const posT = easeOutBack(cProg)
    const rotT = easeOutCubic(cProg)

    const x   = 110 + (p.finalX - 110) * posT          // vw
    const y   = p.finalY * rotT                         // px
    const rot = p.launchRot + (p.finalRot - p.launchRot) * rotT
    const fold = p.fold * Math.sin(Math.min(cProg, 1) * Math.PI)

    el.style.transform = `translate(${x}vw, calc(-50% + ${y}px)) rotate(${rot}deg) rotateY(${fold}deg)`
  }
}

// Continuous RAF loop — lerps visual progress and fades teaser text out
const melLoopFn = () => {
  const section = melSectionRef.value
  if (section) {
    const scrolled = window.scrollY - section.offsetTop
    const range    = section.scrollHeight - window.innerHeight
    const lastCardEnd = MEL_LEAD + (MEL_COUNT - 1) * MEL_STAGGER + (melCardParams[MEL_COUNT - 1].dur ?? MEL_DURATION)
    const raw = Math.max(0, Math.min(1, scrolled / range))
    melTargetProgress = Math.min(1, raw / lastCardEnd)
  }

  const lerpRate = melCurrentProgress < MEL_LEAD ? 0.07 : 0.16
  melCurrentProgress += (melTargetProgress - melCurrentProgress) * lerpRate
  updateMelCards(melCurrentProgress)

  // Text fades out fast: starts when first card launches, gone well before it lands
  const teaserEl = melTeaserRef.value
  if (teaserEl) {
    const fade = Math.max(0, Math.min(1, (melCurrentProgress - MEL_LEAD) / 0.05))
    teaserEl.style.opacity = String(1 - fade)
  }

  melRafId = requestAnimationFrame(melLoopFn)
}

const updateActiveFeature = () => {
  const el = featSectionRef.value
  if (!el) return
  const scrollableRange = el.offsetHeight - window.innerHeight
  if (scrollableRange <= 0) return
  const scrolled = window.scrollY - el.offsetTop
  const clamped = Math.max(0, Math.min(scrolled, scrollableRange))
  const progress = clamped / scrollableRange
  activeFeature.value = Math.min(Math.floor(progress * featureItems.length), featureItems.length - 1)
}

// ── Proof counter animation ──────────────────────────────────────────────────
let proofObserver = null
let animObserver = null
let frameIds = []

const easeOutCubic = (t) => 1 - Math.pow(1 - t, 3)

const animateNumber = (el, endValue, suffix, duration = 1300) => {
  const start = performance.now()

  const tick = (now) => {
    const progress = Math.min((now - start) / duration, 1)
    const eased = easeOutCubic(progress)
    const current = Math.round(endValue * eased)
    el.textContent = `${current}${suffix}`

    if (progress < 1) {
      const id = requestAnimationFrame(tick)
      frameIds.push(id)
    }
  }

  const id = requestAnimationFrame(tick)
  frameIds.push(id)
}

// ── Parallax ──────────────────────────────────────────────────────────────
let parallaxRafId = null
let parallaxHandler = null

const updateParallax = () => {
  const els = document.querySelectorAll('.parallax-inner')
  const vh = window.innerHeight
  els.forEach((el) => {
    const rect = el.getBoundingClientRect()
    // distance of element centre from viewport centre
    const centre = rect.top + rect.height / 2
    const offset = (centre - vh / 2) / vh
    // subtle shift: max ±22px, opposite to scroll direction
    const shift = offset * 60
    el.style.transform = `translateY(${shift}px)`
  })
}

onMounted(() => {
  // Parallax listener
  parallaxHandler = () => {
    if (parallaxRafId) return
    parallaxRafId = requestAnimationFrame(() => {
      updateParallax()
      parallaxRafId = null
    })
  }
  window.addEventListener('scroll', parallaxHandler, { passive: true })
  updateParallax()

  // Feature scroll listener
  featScrollHandler = updateActiveFeature
  window.addEventListener('scroll', featScrollHandler, { passive: true })

  // Proof counter
  const proofSection = document.querySelector('.story-proof-section')
  const numberEls = Array.from(document.querySelectorAll('.proof-number'))
  let hasAnimated = false

  numberEls.forEach((el) => {
    const suffix = el.dataset.suffix || ''
    el.textContent = `0${suffix}`
  })

  // Scroll-enter animations (replay on every re-entry)
  const animEls = Array.from(document.querySelectorAll('.anim-ready, .anim-pop'))
  animObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          // Force reflow so the transition replays from start
          entry.target.classList.remove('anim-in')
          requestAnimationFrame(() => {
            entry.target.classList.add('anim-in')
          })
        } else {
          entry.target.classList.remove('anim-in')
        }
      })
    },
    { threshold: 0.12 }
  )
  animEls.forEach((el) => animObserver.observe(el))

  if (!proofSection || numberEls.length === 0) return

  proofObserver = new IntersectionObserver(
    (entries) => {
      const visible = entries.some((entry) => entry.isIntersecting)
      if (!visible || hasAnimated) return

      hasAnimated = true
      numberEls.forEach((el) => {
        const target = Number(el.dataset.target || '0')
        const suffix = el.dataset.suffix || ''
        animateNumber(el, target, suffix)
      })

      proofObserver?.disconnect()
      proofObserver = null
    },
    { threshold: 0.42 }
  )

  proofObserver.observe(proofSection)

  // Mel card gallery — continuous RAF loop
  melRafId = requestAnimationFrame(melLoopFn)
})

onBeforeUnmount(() => {
  if (parallaxHandler) {
    window.removeEventListener('scroll', parallaxHandler)
    parallaxHandler = null
  }
  if (parallaxRafId) {
    cancelAnimationFrame(parallaxRafId)
    parallaxRafId = null
  }
  if (featScrollHandler) {
    window.removeEventListener('scroll', featScrollHandler)
    featScrollHandler = null
  }
  animObserver?.disconnect()
  animObserver = null
  proofObserver?.disconnect()
  proofObserver = null
  frameIds.forEach((id) => cancelAnimationFrame(id))
  frameIds = []
  if (melRafId) { cancelAnimationFrame(melRafId); melRafId = null }
  exploreObserver?.disconnect()
  exploreObserver = null
})
</script>
